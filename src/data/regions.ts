// 구 형식을 새 형식으로 변환하는 헬퍼 함수
export const convertRegionFormat = (province: string, district: string): string => {
  return `${province.replace('특별시', '').replace('광역시', '').replace('특별자치시', '').replace('특별자치도', '').replace('도', '')} ${district}`;
};

// 새 형식을 구 형식으로 변환하는 헬퍼 함수  
export const parseRegionFormat = (region: string): { province: string; district: string } | null => {
  const parts = region.split(' ');
  if (parts.length !== 2) return null;
  
  const [shortProvince, district] = parts;
  
  // 짧은 형식에서 전체 이름으로 변환
  const provinceMap: Record<string, string> = {
    '서울': '서울특별시',
    '부산': '부산광역시',
    '대구': '대구광역시',
    '인천': '인천광역시',
    '광주': '광주광역시',
    '대전': '대전광역시',
    '울산': '울산광역시',
    '세종': '세종특별자치시',
    '경기': '경기도',
    '강원': '강원도',
    '충북': '충청북도',
    '충남': '충청남도',
    '전북': '전라북도',
    '전남': '전라남도',
    '경북': '경상북도',
    '경남': '경상남도',
    '제주': '제주특별자치도'
  };
  
  const fullProvince = provinceMap[shortProvince];
  return fullProvince ? { province: fullProvince, district } : null;
};

